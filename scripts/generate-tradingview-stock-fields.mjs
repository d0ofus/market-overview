import { writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SOURCE_URL = "https://shner-elmo.github.io/TradingView-Screener/fields/stocks.html";

function decodeHtml(value) {
  return value
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&nbsp;/g, " ");
}

function stripTags(value) {
  return decodeHtml(value.replace(/<[^>]+>/g, " ").replace(/\s+/g, " ").trim());
}

function humanizeFieldName(field) {
  return field
    .replace(/[._]+/g, " ")
    .replace(/\|/g, " | ")
    .replace(/\s+/g, " ")
    .trim()
    .replace(/\b\w/g, (char) => char.toUpperCase());
}

function extractRowValues(nameCell) {
  const listItems = [...nameCell.matchAll(/<li>([\s\S]*?)<\/li>/gi)].map((match) => stripTags(match[1]));
  if (listItems.length > 0) return listItems.filter(Boolean);
  const summary = nameCell.match(/<summary>([\s\S]*?)<\/summary>/i);
  if (summary) return [stripTags(summary[1])].filter(Boolean);
  return [stripTags(nameCell)].filter(Boolean);
}

const response = await fetch(SOURCE_URL);
if (!response.ok) {
  throw new Error(`Failed to fetch ${SOURCE_URL}: ${response.status} ${response.statusText}`);
}

const html = await response.text();
const tbodyMatch = html.match(/<tbody>([\s\S]*?)<\/tbody>/i);
if (!tbodyMatch) throw new Error("Could not find <tbody> in TradingView stock fields page.");
const lastUpdated = stripTags(html.match(/<mark>\s*Last updated:\s*([\s\S]*?)<\/mark>/i)?.[1] ?? "");
const rows = [...tbodyMatch[1].matchAll(/<tr>([\s\S]*?)<\/tr>/gi)].map((match) => match[1]);
const byValue = new Map();

for (const row of rows) {
  const cells = [...row.matchAll(/<td>([\s\S]*?)<\/td>/gi)].map((match) => match[1]);
  if (cells.length < 3) continue;
  const values = extractRowValues(cells[0]);
  const label = stripTags(cells[1]);
  const type = stripTags(cells[2]);
  for (const value of values) {
    const normalizedValue = value.trim();
    if (!normalizedValue) continue;
    const existing = byValue.get(normalizedValue);
    if (existing) {
      if (!existing.label && label) existing.label = label;
      if (!existing.type && type) existing.type = type;
      continue;
    }
    byValue.set(normalizedValue, {
      value: normalizedValue,
      label: label || humanizeFieldName(normalizedValue),
      type: type || "unknown",
    });
  }
}

const fields = [...byValue.values()].sort((left, right) => {
  const labelComparison = left.label.localeCompare(right.label);
  if (labelComparison !== 0) return labelComparison;
  return left.value.localeCompare(right.value);
});

const output = `// Generated by scripts/generate-tradingview-stock-fields.mjs
// Source: ${SOURCE_URL}
// Source last updated: ${lastUpdated || "unknown"}

export type TradingViewStockField = {
  value: string;
  label: string;
  type: string;
};

export const TRADINGVIEW_STOCK_FIELDS: TradingViewStockField[] = ${JSON.stringify(fields, null, 2)};\n`;

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outputPath = path.resolve(__dirname, "..", "web", "lib", "tradingview-stock-fields.ts");
await writeFile(outputPath, output, "utf8");
console.log(`Wrote ${fields.length} fields to ${outputPath}`);
